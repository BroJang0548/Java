package com.brojang.java.ex02.variable;

public class VarTest3 {
    public static void main(String[] args){
        // 진수 표기
        int i1 = 10; // 10진수
        System.out.println(i1);

        int i2 = 0100; // 8진수
        System.out.println(i2);

        int i3 = 0x1234; // 16진수
        System.out.println(i3);

    }
}
