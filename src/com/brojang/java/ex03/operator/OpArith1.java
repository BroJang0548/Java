package com.brojang.java.ex03.operator;

public class OpArith1 {
    public static void main(String[] args) {
        // 연산자
        int val1 = 5;
        int val2 = 10;
        int result = 0;

        result = val1 + val2;
        System.out.println(result);
        result = val1 - val2;
        System.out.println(result);
        result = val1 * val2;
        System.out.println(result);
        result = val1 / val2;
        System.out.println(result);
        result = val1 % val2;
        System.out.println(result);
    }
}
