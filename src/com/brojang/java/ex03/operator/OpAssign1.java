public class OpAssign1 {
    public static void main(String[] args) {
        int result = 0;
        result +=10;
        System.out.println(result);

        result -=5;
        System.out.println(result);

        result *=2;
        System.out.println(result);

        result /=4;
        System.out.println(result);

    }
}
