package com.brojang.java.ex03.operator.casting;

public class RealTest2 {
    public static void main(String[] args) {
        float fNum = 0f;
        System.out.println(fNum);
        fNum = 5.0f;
        System.out.println(fNum);
        fNum = 7;
        System.out.println(fNum);

        int num = 0;
        System.out.println(num);
        num = 5;
        System.out.println(num);
        num = 5/3;
        System.out.println(num);
    }
}
